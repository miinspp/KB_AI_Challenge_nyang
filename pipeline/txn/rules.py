def classify_rule(t: Txn) -> Optional[tuple[str, float, str]]:
    """텍스트 키워드 규칙으로 분류. 반환 (code, confidence, reason) 또는 None."""
    text = f"{t.desc} {t.counterparty or ''} {t.merchant_norm or ''}".lower()
    best: Optional[tuple[str, float, str]] = None

    for code, need_dir, keywords, conf in RULES:
        if need_dir and t.direction != need_dir:
            continue
        hit = next((k for k in keywords if k.lower() in text), None)
        if hit and (best is None or conf > best[1]):
            best = (code, conf, f"키워드 '{hit}' 매칭")

    return best
